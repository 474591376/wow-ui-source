function FeedbackUI_GetLocalizedCharString (level, race, gender, class)
    if ( GetLocale() == "deDE" ) then
        return FEEDBACKUI_LEVELPREFIX .. " " .. level .. " " .. race .. " " .. class .. " " .. gender;
    else
        return FEEDBACKUI_LEVELPREFIX .. " " .. level .. " " .. race .. " " .. gender .. " " .. class;
    end
end


